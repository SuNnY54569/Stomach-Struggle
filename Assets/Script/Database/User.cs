
public class User
{
    public string name;
    public string preTestScore;
    public string postTestScore;
    public string totalHeart;

    public User(string name, string preTestScore, string postTestScore, string totalHeart)
    {
        this.name = name;
        this.preTestScore = preTestScore;
        this.postTestScore = postTestScore;
        this.totalHeart = totalHeart;
    }
}
